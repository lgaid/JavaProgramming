package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Hello Students\nToday we have Java class");

        System.out.println("--------------------------------");

        System.out.println("\tJava Programming");

        System.out.println("--------------------------------");

        System.out.println("\\");

        System.out.println("--------------------------------");

        System.out.println("My favorite show is \"Supergirl\"");



    }


}
/*
1. \n: New Line
2. \t: Paragraph space
3. \\: Single back slash
4. \": Double quote

 */
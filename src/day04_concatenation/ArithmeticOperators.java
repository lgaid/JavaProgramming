package day04_concatenation;

public class ArithmeticOperators {


    public static void main(String[] args) {


        System.out.println( 100 + 200 ); // 200

        System.out.println( 40 - 10 );  // 30

        System.out.println( 20 / 6 ); // only give the int sum (not the decimal)

        System.out.println( 50 / 6.0 ); // By adding a .0 you will get a precise answer

        System.out.println( 50d / 6 );  // d is shortcut to get decimal

        System.out.println( (int) 2.5 / 0.5 );

        //  System.out.println( 9 / 0); never divide by 0 it will give error

        System.out.println( 20 * 12 );


        System.out.println( 100 % 13 );   // Modulus

        System.out.println( 45 % 8 );

        System.out.println( (int) 3.5 / 0.5 );













    }






}

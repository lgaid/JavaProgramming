package day03_variables;

public class ReplaceVariableName {

    public static void main(String[] args) {

        String name = "Josh";

        System.out.println(name);
        System.out.println(name);
        System.out.println(name);
        System.out.println(name);
        System.out.println(name);


        double salary = 100000.5;

        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);

    }
}
